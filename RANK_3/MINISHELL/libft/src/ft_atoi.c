long int	ft_atoi(const char *nptr)
{
	char			*s;
	char			neg;
	long int		res;

	s = (char *)nptr;
	res = 0;
	neg = 0;
	while ((*s >= '\t' && *s <= '\r') || *s == ' ')
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			neg = 1;
		s++;
	}
	while (*s >= '0' && *s <= '9')
		res = res * 10 + *s++ - '0';
	if (neg)
		res *= -1;
	return (res);
}
